# Variables in Python do not need the explicit datatype tag
# The datatype is taken from the value of the Variables

a = 1       # makes 'a' an int
b = 2.0     # makes 'b' a float
c = "Hi"    # makes 'c' a string
d = 'a'     # makes 'd' a string, since Python doesnt explicitly use the character datatype

# To check the data-type of your variable:
print(type(d))
